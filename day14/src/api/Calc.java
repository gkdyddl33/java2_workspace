package api;

public class Calc {
	
	/**
	 * To div the following integers.<br>
	 * For example<br>
	 * <code> div(10,3) : 3</code>
	 * 
	 * @param num1 : First integer for div
	 * @param num2 : Last integer for div
	 * @return int
	 * 
	 * @see Math
	 * @exception ArithmeticException
	 */
	public int div(int num1,int num2) {
		return num1/num2;
	}
}
